const InputEnglish = document.getElementById("english");
const InputHindi = document.getElementById("hindi");
const InputMarathi = document.getElementById("marathi");

const generateBtn = document.getElementById("generate");
const copyBtn = document.getElementById("copy");
const displayjoke = document.getElementById("displayJoke");



const generateJoke = () => {
    if (InputEnglish.checked) {
        jokeFunction(englishJokes)
    }
    else if (InputHindi.checked) {
        jokeFunction(hindiJokes)
    }
    else if (InputMarathi.checked) {
        jokeFunction(marathiJokeS)
    }
    else {
        displayjoke.innerHTML = "Please Select language!!!"
    }
}

const jokeFunction = (arr) => {
    let randomNumber = Math.floor(Math.random() * (arr.length))
    document.getElementById("displayJoke").innerText = arr[randomNumber]
}

generateBtn.addEventListener("click", function () {
    copyBtn.innerText = "Copy"
    generateJoke()
})
copyBtn.addEventListener("click", function () {
    displayjoke.select();
    displayjoke.setSelectionRange(0, 99999);
    navigator.clipboard.writeText(displayjoke.value);
    setTimeout(() => {
        copyBtn.innerText = 'Copied'
    }, 200)
    clearTimeout();

})

